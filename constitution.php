	<div id="gm">
		<h3>Article 4 – CSSS General Meetings (GM)</h3>
		<h4 class="subtitle">4.1 Scheduling of GM</h4>
		Two GM meetings required. One at the start of Term 1, and the second at the end of Term 2.
		<br><br>Emergency GM
		<ul>
			<li>An Executive Committee member can raise the motion of an Emergency GM</li>
			<li>During an Executive meeting when 80% of the Executive Committee are present, a majority vote of all members present passes the motion.</li>
			<li>Schedule the Emergency GM as soon as possible.</li>
			<li>For all purposes, an Emergency GM is exactly the same as a normal GM.</li>
		</ul>
		<br>

		<h4 class="subtitle">4.2 GM Structure</h4>
		AGM Meetings proceed in the order through the following parts
		<br>Part 1: Beginning and Meet and Greet
		<ul>
			<li>President calls the AGM to begin</li>
			<li>Executive members introduce themselves and discuss their roles.</li>
		</ul>
		Part 2: CSSS Status Update
		<ul>
			<li>Presidential plans and ideas for the coming year.</li>
			<li>Executive members discuss previous years events.</li>
			<li>Treasurer and Secretary give a brief account of previous years achievements and current financial state.</li>
		</ul>
		Part 3: Open Q&A
		<ul>
			<li>All people present at the AGM can ask questions to the Executive Committee.</li>
		</ul>
		Part 4: Election of Executive Committee
		<br>Starting from the highest Rank, all nominees:
		<ul>
			<li>Nominees that meet the eligibility requirements have priority over nominees that do not, as discussed in section 3.1</li>
			<li>Nominees present themselves and give a short speech on why they should be elected.</li>
			<li>All CSSS members present then vote, including nominees.</li>
			<li>The President counts the votes and announces the victor</li>
			<li>Proceed to next role/rank</li>
		</ul>
		Part 5: Raise Motions
		<ul>
			<li>All CSSS members can raise motions.</li>
			<li>The member raising the motion presents the motion.</li>
		</ul>
		Part 6: Break
		<ul>
			<li>10-30min break is held.</li>
			<li>CSSS provides food and drinks</li>
		</ul>
		Part 7: Vote on Motions
		<ul>
			<li>All members vote on motions raised from Part 5.</li>
			<li>President counts vote and announces pass/fail, except on motions raised by the president himself.</li>
		</ul>
		Part 8: Conclusion
		<ul>
			<li>Brief second Q&A for everyone present</li>
			<li>President announces end of meeting</li>
		</ul>
		<br>
	</div>


