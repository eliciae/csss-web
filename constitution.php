		<h4 class="subtitle">3.1.4 Suspension of CSSS executives</h4>
		Raising an Executive Suspension
		<ul>
			<li>Raising a motion to suspend an Executive Committee member can only be initiated during an Executive Meeting when 80% of the Executive Committee is present.</li>
			<li>The executive in question is required to leave the room until voting is resolved.</li>
		</ul>
		Resolving Raised suspension
		<ul>
			<li>A 2/3 of Executive Committee present must vote in favor of suspension.</li>
		</ul>
		Suspension of Executive Member
		<ul>
			<li>Member is immediately relieved of all duties and powers for one month.</li>
			<li>Executive Members reassign suspended members’ duties.</li>
		</ul>
		<br>

		<h4 class="subtitle">3.1.5 Expulsion of CSSS Executives</h4>
		Conditions for Expulsion
		<ul>
			<li>The executive in question has intentionally failed to fulfill duties</li>
			<li>conducted themselves in such a fashion as to damage the reputation of the CSSS</li>
			<li>or has intentionally harmed the well being of a fellow member or staff</li>
		</ul>
		Raising an Executive Expulsion
		<ul>
			<li>Raising a motion to expel an Executive Committee member can only be initiated during an Executive Meeting when 80% of the Executive Committee are present.</li>
			<li>The executive in question is required to leave the room until voting is resolved.</li>
		</ul>
		Resolving Raised Expulsion
		<ul><li>2/3 of Executive Committee present must vote in favor of expulsion.</li></ul>
		Expulsion of the President
		<ul>
			<li>Must be raised by an Executive Committee member other than the President or Vice President</li>
			<li>This motion cannot be vetoed by the President.</li>
		</ul>   
		Expulsion of Non-President Executive Committee member can be raised by any other Executive Committee member.
		<br>
		Expulsion of a non-President Executive:
		<ul>
			<li>Member is immediately relieved of duties and powers, and removed from the Executive Committee.</li>
			<li>Remaining executives then delegate expelled members duties to remaining executives</li>
		</ul>
		Expulsion of the President:
		<ul>
			<li>President is immediately relieved of duties and powers, and removed from the Executive Committee.</li>
			<li>Vice President takes over all Presidential duties and responsibilities.</li>
		</ul>
		<br>
		

