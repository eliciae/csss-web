			<li>Members are allowed to attend all CSSS meetings, including Executive Meetings. </li>
		</ul>

		<h4>3.2.2 Expulsion of Non-Executive Member</h4>
		Conditions for Expulsion
		<ul>
			<li>The member in question has intentionally conducted themselves in such a fashion as to damage the reputation of the CSSS, or has intentionally harmed the well being of a fellow member or staff, is eligible for a motion of expulsion.</li>
		</ul>
		 Raising A Motion of Expulsion
		<ul>
			<li>Any executive can raise a motion of expulsion only during an Executive Meeting when 60% of the Executive Committee are present, and the member in question is present.</li>
		</ul>
		Resolving Motion of Expulsion
		<ul>
			<li>Member in question is required to leave the room during voting.</li>
			<li>The motion is passed with a majority vote.</li>
